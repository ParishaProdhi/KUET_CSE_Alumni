CREATE TABLE ALUMNI(
	ROLL_NO NUMBER(7) NOT NULL,
	ALUMNI_ID NUMBER(2) NOT NULL,
	NAME VARCHAR(20) UNIQUE,
	BATCH VARCHAR(4),
	PRESENT_ADDRESS VARCHAR(20),
	CONTACT_NO NUMBER(20),
	BLOOD_GROUP VARCHAR(3),
	CGPA NUMBER check(CGPA>=0 and CGPA<=4)
	--primary key(ROLL_NO)
);

create table CAMPUS_ASSOCIATIONS(
	ROLL_NO NUMBER(7) ,
	DREAM VARCHAR(10),
	KDS VARCHAR(10),
	PROTIDDHONI VARCHAR(10),  
	KUET_THEATRE VARCHAR(10)
	--foreign key(ROLL_NO) references ALUMNI(ROLL_NO)
);

CREATE TABLE EXPERIENCES(
	ROLL_NO NUMBER(7) ,
	PROJECT_PLATFORMS VARCHAR(50),
	THESIS_TOPICS VARCHAR(50),
	DATEofGraduation Date
	--PRIMARY key(ROLL_NO),
	--foreign key(ROLL_NO) references ALUMNI(ROLL_NO)
);

CREATE TABLE PRESENT_WORKS(
	NATIONAL_id NUMBER(12),
	ROLL_NO NUMBER(7) ,
	JOB VARCHAR(50),
	PLATFORM VARCHAR(50),
	EXPERT_AREA VARCHAR(50),
	primary key(NATIONAL_id)
	--foreign key(ROLL_NO) references ALUMNI(ROLL_NO)
);





